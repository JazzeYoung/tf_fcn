def resize_gt_boxes(gt_boxes, scale_ratio):
    xys, cls = \
            gt_boxes[:, 0:4], gt_boxes[:, 4]
    xys = xys * scale_ratio 
    return tf.concat(values=(xys, cls[:, tf.newaxis]), axis=1)

